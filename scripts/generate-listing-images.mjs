for (const id of PEXELS_PROPERTY) {
  const url = pexels(id);
  if (await headOk(url)) pool.push({ type: 'pexels', id: String(id), url });
}

const uniqueUrls = new Set();
const deduped = pool.filter((p) => {
  if (uniqueUrls.has(p.url)) return false;
  uniqueUrls.add(p.url);
  return true;
});

console.log('Photo pool:', deduped.length);

if (deduped.length < 150) {
  console.error('Need 150 photos, got', deduped.length);
  process.exit(1);
}

const SETS = {};
let i = 0;
for (const lid of LISTING_IDS) {
  const slice = deduped.slice(i, i + 5);
  SETS[lid] = {
    thumbnail: slice[0].url.replace('w=1200', 'w=600').replace('h=800', 'h=400'),
    images: slice.map((p) => p.url),
  };
  i += 5;
}

