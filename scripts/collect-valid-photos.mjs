import fs from 'fs';
import path from 'path';

function walk(dir, acc = []) {
  for (const f of fs.readdirSync(dir, { withFileTypes: true })) {
    if (['node_modules', '.next', 'scripts'].includes(f.name)) continue;
    const p = path.join(dir, f.name);
    if (f.isDirectory()) walk(p, acc);
    else if (/\.(ts|tsx)$/.test(f.name)) acc.push(p);
  }
  return acc;
}

const ids = new Set();
for (const file of walk('.')) {
  const t = fs.readFileSync(file, 'utf8');
  for (const m of t.matchAll(/photo-([a-z0-9-]+)/g)) ids.add(m[1]);
}

const valid = [];
for (const id of ids) {
  const res = await fetch(`https://images.unsplash.com/photo-${id}?w=200&h=200&fit=crop`, {
    method: 'HEAD',
    redirect: 'follow',
  });
  if (res.ok) valid.push(id);
}

